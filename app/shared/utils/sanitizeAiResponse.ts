/**
 * Очищает текст ответа AI от служебных JSON-маркеров.
 *
 * Бэкенд возвращает в конце ответа строку вида:
 *   {"cards": [23, 20, 17, 47, 55]}
 *
 * Этот JSON используется бэком для подсветки card_ids — на клиенте мы
 * получаем уже распарсенные `referenced_cards` отдельным полем, поэтому
 * сам маркер из текста надо убирать.
 *
 * Удаляем все JSON-объекты с ключом "cards" в любом месте текста, плюс
 * лишние пустые строки/пробелы по краям.
 */
export function sanitizeAiResponse(raw: string | null | undefined): string {
  if (!raw) return "";

  // Удаляем JSON-объекты вида {"cards": [...]} (с любыми пробелами/переносами)
  const withoutCardsJson = raw.replace(
    /\{\s*"cards"\s*:\s*\[[^\]]*\]\s*\}/g,
    ""
  );

  // Чистим висящие пустые строки в конце и в начале
  return withoutCardsJson.trim().replace(/\n{3,}/g, "\n\n");
}
